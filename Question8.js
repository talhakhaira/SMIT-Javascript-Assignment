
function testPrime(n)
{
  if (n===1)
  {
    return false;
  }
  else if(n === 2)
  {
    return true;
  }else
  {
    for(var x = 2; x < n; x++)
    {
      if(n % x === 0)
      {
        return false;
      }
    }
      return true;  
  }
}


var num = prompt("Enter a number: ");

alert((testPrime(num)));